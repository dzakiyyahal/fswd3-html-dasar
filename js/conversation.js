// TYPE CONVERSATION
// num to string
let a = 20;
console.log(`Nilai a = ${a}`);
console.log(`Tipe data a = `);
console.log(typeof a);
console.log(`Konversi tipe data a. Setelah konversi, tipe data a menjadi = `);
a = String(a);
console.log(typeof a);

// string to number
let str = "123";
console.log(`Nilai str = ${str}`);
console.log(`Tipe data str = `);
console.log(typeof str);
console.log(`Konversi tipe data str. Setelah konversi, tipe data str menjadi = `);
str = Number(str);
console.log(typeof str);

// konversi boolean
console.log(`Konversi Boolean = `);
console.log(Boolean(1));
console.log(Boolean(0));